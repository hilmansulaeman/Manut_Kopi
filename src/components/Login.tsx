export default function Login({ onLogin }: { onLogin: () => void }) {
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [error, setError] = useState("");

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setError(""); // Clear previous errors
    console.log('Attempting login with:', { email, password });

    try {
      const response = await fetch('/api/auth/login', { // Changed to relative path for proxy
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ email, password }),
      });

      const data = await response.json();
      console.log('Login API response status:', response.status);
      console.log('Login API response data:', data);

      if (response.ok) {
        localStorage.setItem('token', data.token); // Store the JWT token
        onLogin();
      } else {
        setError(data.message || 'Login failed');
      }
    } catch (err) {
      setError('Failed to connect to the server. Please try again later.');
      console.error('Login error:', err);
    }
  };

  return (
    <div className="bg-[#313131] relative size-full flex items-center justify-center p-4">
      <BackgroundGraphics />
      
      <div className="relative z-10 w-full max-w-md px-4 sm:px-6">
        <div className="flex flex-col items-center mb-8 sm:mb-12">
          <div className="h-[60px] w-[95px] sm:h-[80px] sm:w-[125px] mb-4">
            <img 
              alt="MANUT KOPI" 
              className="w-full h-full object-contain" 
              src={imgLogoManutRemovebgPreview1} 
            />
          </div>
          <h1 className="font-['Inter:Bold',_sans-serif] text-2xl sm:text-[32px] text-white">MANUT KOPI</h1>
        </div>

        <form onSubmit={handleSubmit} className="space-y-6">
          <div className="space-y-2">
            <Label htmlFor="email" className="text-white">Email</Label>
            <Input
              id="email"
              type="email"
              placeholder="Enter your email"
              value={email}
              onChange={(e) => setEmail(e.target.value)}
              className="bg-white/10 border-white/20 text-white placeholder:text-white/50 focus:border-white/40 focus:ring-white/20"
              required
            />
          </div>

          <div className="space-y-2">
            <Label htmlFor="password" className="text-white">Password</Label>
            <Input
              id="password"
              type="password"
              placeholder="Enter your password"
              value={password}
              onChange={(e) => setPassword(e.target.value)}
              className="bg-white/10 border-white/20 text-white placeholder:text-white/50 focus:border-white/40 focus:ring-white/20"
              required
            />
          </div>

          {error && (
            <div className="text-red-400 text-sm text-center p-2 bg-red-500/10 rounded-lg border border-red-500/20">
              {error}
            </div>
          )}

          <Button 
            type="submit" 
            className="w-full bg-white text-[#313131] hover:bg-white/90"
          >
            Login
          </Button>
        </form>

        <div className="mt-6 text-center">
          <a href="#" className="text-white/70 hover:text-white transition-colors">
            Forgot password?
          </a>
        </div>
      </div>
    </div>
  );
}
